using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyCalcLib
{
    public class MyCalc
    {
        public int abs (int x, int y)
        {
            return x * y ;
        }
        public int Sum (int x, int y)
        {
            return x + y ;
        }

        public int divide (int x, int y)
        {
            return x / y ;
        }

        public int subtract(int x, int y)
        {
            return x - y ;
        }
    }
}
